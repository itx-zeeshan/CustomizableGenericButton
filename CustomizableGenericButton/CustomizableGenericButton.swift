import Foundation

public final class CustomizableGenericButton {

    let name = "CustomizableGenericButton"
    
    public func add(a: Int, b: Int) -> Int {
        return a + b
    }
    
    public func sub(a: Int, b: Int) -> Int {
        return a - b
    }
    
}
